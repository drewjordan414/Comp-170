public class epressions3 {
    public static void main(String[] args){
        // 
        // 2 + 2 + 3 + 4	
        //"2 + 2 " + 3 + 4	
        // 2 + " 2 + 3 " + 4	
        // 3 + 4 + " 2 + 2"	
        // "2 + 2 " + (3 + 4)	
        // "(2 + 2) " + (3 + 4)	
        // "hello 34 " + 2 * 4	
        // 2 + "(int) 2.0" + 2 * 2 + 2	
        // 4 + 1 + 9 + "." + (-3 + 10) + 11 / 3	
        // 8 + 6 * -2 + 4 + "0" + (2 + 5)	
        // 1 + 1 + "8 - 2" + (8 - 2) + 1 + 1	
        // 5 + 2 + "(1 + 1)" + 4 + 2 * 3	
        // "1" + 2 + 3 + "4" + 5 * 6 + "7" + (8 + 9)
        int math1 = 2 + 2 + 3 + 4;
        System.out.println(math1);
        String math2 = "2 + 2 " + 3 + 4;
        System.out.println(math2);
        String math3 = 2 + " 2 + 3 " + 4;
        System.out.println(math3);
        String math4 = 3 + 4 + " 2 + 2";
        System.out.println(math4);
        String math5 = "2 + 2 " + (3 + 4);
        System.out.println(math5);
        String math6 = "(2 + 2) " + (3 + 4);
        System.out.println(math6);
        String math7 = "hello 34 " + 2 * 4;
        System.out.println(math7);
        String math8 = 2 + "(int) 2.0" + 2 * 2 + 2;
        System.out.println(math8);
        String math9 = 4 + 1 + 9 + "." + (-3 + 10) + 11 / 3;
        System.out.println(math9);
        String math10 = 8 + 6 * -2 + 4 + "0" + (2 + 5);
        System.out.println(math10);
        String math11 = 1 + 1 + "8 - 2" + (8 - 2) + 1 + 1;
        System.out.println(math11);
        String math12 = 5 + 2 + "(1 + 1)" + 4 + 2 * 3;
        System.out.println(math12);
        String math13 = "1" + 2 + 3 + "4" + 5 * 6 + "7" + (8 + 9);
        System.out.println(math13);
    }
}
